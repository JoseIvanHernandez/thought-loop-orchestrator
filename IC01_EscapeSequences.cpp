#include <iostream>
using namespace std;

int main() {
    cout << "Escape sequences in C++:\n"
         << "Newline: \\n\n"
         << "Tab: \\t\n"
         << "Backslash: \\\\\n"
         << "Single quote: \\\'\n"
         << "Double quote: \\\"\n"
         << "Question mark: \\?\n"
         << "Alert (beep): \\a\n"
         << "Backspace: \\b\n"
         << "Form feed: \\f\n"
         << "Carriage return: \\r\n"
         << "Vertical tab: \\v\n";
    return 0;
}
